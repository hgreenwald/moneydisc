  def _make_turnover_correction(self, row):
    """
    Compute the GPA of a turnover. Just use the negative game efficiency for now.
    Essentially this is the likelihood of either team scoring, so it's a rough
    approximation of the lost value of the turnover.

    If the throw wasn't a turnover, then the value stays the same.
    """
    return (1 - row['scoring_prob_end_opp']) - row['scoring_prob_start'] if row['turnover']==1 else row['gpa']

  def get_game_efficiency(self, game_id):
    """Return the overall scoring efficiency from a certain game."""
    return self.game_stats_df.loc[self.game_stats_df['game_id']==game_id, 'combined_scoring_efficiency'].values[0]

  def get_players(self):
    """Get a list of all the players."""
    players = []
    for game_id, df in self.player_stats_dfs.items():
      players += list(df['player'].unique())

    return list(set(players))

  def _compute_game_stats(self):
    """Compute the game-level stats, which we'll also need to compute all the player level stats."""
    for game_id, name in self.games.items():
      points_df = self.points_dfs[game_id]
      passes_df = self.passes_dfs[game_id]
      possessions_df = self.possessions_dfs[game_id]
      game_stats = {}
      game_stats['game_id'] = game_id
      game_stats['game_name'] = name
      points_df['opponent_possessions'] = points_df.apply(self._compute_opponent_possessions, axis=1)
      game_stats['possessions'] = points_df['possessions'].sum()
      game_stats['opponent_possessions'] = points_df['opponent_possessions'].sum()
      game_stats['passes'] = points_df['passes'].sum()
      game_stats['points'] = points_df['point'].max()-1. # there's an extra row in the df for the final score
      game_stats['our_score'], game_stats['opponent_score'] = self._get_final_score(points_df)
      game_stats['our_scoring_efficiency'] = game_stats['our_score']/game_stats['possessions']
      game_stats['opponent_scoring_efficiency'] = game_stats['opponent_score']/game_stats['opponent_possessions']
      game_stats['combined_scoring_efficiency'] = (game_stats['our_score'] + game_stats['opponent_score'])/(game_stats['opponent_possessions'] + game_stats['possessions'])
      self.game_stats_df = pd.concat([self.game_stats_df, pd.DataFrame([game_stats])], ignore_index=True)

  def _compute_player_stats(self, df):
      """Given an aggregated player dataframe, compute some extra stats."""
      # normalize the gpa
      df['adjusted_offensive_gpa'] = df['total_offensive_gpa']/(df['max_gpa'])
      df['adjusted_thrower_gpa'] = df['thrower_gpa']/(df['max_gpa'])
      df['adjusted_receiver_gpa'] = df['receiver_gpa']/(df['max_gpa'])
      df['total_gpa'] = df['total_offensive_gpa'] + df['total_defensive_gpa']

      # traditional +/-
      df['off. plus/minus'] = df['goals'] + df['assists'] - df['total_turnovers']
      df['off. real plus/minus'] = (df['goals'] + df['assists'])/2 - df['total_turnovers']

      # effective throwing and recieving meters, taking into account lateral movement
      vert_coef = SB.possession_model.params.values[1]
      horz_coef = SB.possession_model.params.values[2]
      df['gpa_vert_throwing'] = df['throwing_meters_gained']*-vert_coef
      df['gpa_horz_throwing'] = df['throwing_centering_meters_gained']*horz_coef
      df['gpa_total_throwing'] = df['gpa_vert_throwing'] + df['gpa_horz_throwing']
      df['gpa_vert_receiving'] = df['receiving_meters_gained']*-vert_coef
      df['gpa_horz_receiving'] = df['receiving_centering_meters_gained']*-horz_coef
      df['gpa_total_receiving'] = df['gpa_vert_receiving'] + df['gpa_horz_receiving']

      # completion percentage stats
      df['completion_percentage'] = df['completions']/df['throws']

      df = self.reorder_player_stat_columns(df)
      return df

  @staticmethod
  def _compute_opponent_possessions(point_row):
    """Function to compute the total number of possessions per point, given our data.

       If we started on offense AND scored, we ended up with one more possession.
       If we started on offense XOR scored, we ended up with the same number of possessions.
       If we did neither, we had one fewer possession.
    """
    a = point_row['scored'] + point_row['started_on_offense']
    return point_row['possessions'] - (a-1)

