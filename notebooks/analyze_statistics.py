def permutation_test(model, X, y, n_perms=1000):
    model.fit(X, y)
    orig_coefs = model.coef_[0].copy()

    perm_coefs = np.zeros((n_perms, X.shape[1]))
    for i in range(n_perms):
        y_perm = np.random.permutation(y)
        model.fit(X, y_perm)
        perm_coefs[i] = model.coef_[0]

    pvals = np.mean(np.abs(perm_coefs) >= np.abs(orig_coefs), axis=0)
    return orig_coefs, pvals

