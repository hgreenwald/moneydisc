def bootstrap_logit_player_effects(
    X, y, n_boot=2000, C=1.0, penalty="l2", solver="liblinear", random_state=42
):
    """
    Stratified bootstrap so each resample has both classes.
    Returns:
      coef_df: per-feature summary (means, 95% CI, odds ratios, P(beta>0))
      boot_mat: (n_boot x p) matrix of bootstrapped coefficients
    """
    rng = np.random.default_rng(random_state)
    model = LogisticRegression(penalty=penalty, C=C, solver=solver, max_iter=2000)

    # indices by class
    idx0 = np.where(y.values == 0)[0]
    idx1 = np.where(y.values == 1)[0]
    n0, n1 = len(idx0), len(idx1)

    if n0 == 0 or n1 == 0:
        raise ValueError("Your original dataset has only one class; cannot fit logistic regression.")

    # keep original class proportions in each bootstrap sample
    n = len(y)
    w1 = n1 / n
    n1_b = max(1, int(round(w1 * n)))
    n0_b = max(1, n - n1_b)

    p = X.shape[1]
    cols = X.columns.tolist()
    boot_mat = np.zeros((n_boot, p), dtype=float)

    b = 0
    attempts = 0
    max_attempts = n_boot * 5  # safety to avoid infinite loop

    while b < n_boot and attempts < max_attempts:
        attempts += 1
        # sample WITH replacement inside each class
        samp0 = rng.choice(idx0, size=n0_b, replace=True)
        samp1 = rng.choice(idx1, size=n1_b, replace=True)
        idx_b = np.concatenate([samp0, samp1])
        rng.shuffle(idx_b)

        Xb = X.iloc[idx_b]
        yb = y.iloc[idx_b]

        # guard: should always have both classes, but we double-check
        if yb.nunique() < 2:
            continue

        try:
            model.fit(Xb, yb)
            boot_mat[b] = model.coef_[0]
            b += 1
        except Exception:
            # very occasionally a solver hiccup; just resample
            continue

    if b < n_boot:
        print(f"Warning: only completed {b}/{n_boot} successful bootstrap fits.")

    # summary
    mean = boot_mat[:b].mean(axis=0)
    lo = np.percentile(boot_mat[:b], 2.5, axis=0)
    hi = np.percentile(boot_mat[:b], 97.5, axis=0)
    prob_pos = (boot_mat[:b] > 0).mean(axis=0)

    odds_mean = np.exp(mean)
    odds_lo = np.exp(lo)
    odds_hi = np.exp(hi)

    coef_df = pd.DataFrame({
        "feature": cols,
        "coef_mean": mean,
        "coef_lo": lo,
        "coef_hi": hi,
        "odds_ratio": odds_mean,
        "odds_lo": odds_lo,
        "odds_hi": odds_hi,
        "prob_coef_gt_0": prob_pos
    }).set_index("feature")

    return coef_df, boot_mat[:b]

