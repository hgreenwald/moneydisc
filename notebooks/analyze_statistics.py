  def prepare_data(self):
    self.load_data()
    self._rename_columns()
    self._fix_player_names()
    self._transform_pass_stats()

  def _rename_columns(self):
    """Get more consistent and easier to use column names"""

    for game_id, df in self.passes_dfs.items():
      df.rename({
          'Start X (0 -> 1 = left sideline -> right sideline)': 'start_x',
          'Start Y (0 -> 1 = back of opponent endzone -> back of own endzone)': 'start_y',
          'End X (0 -> 1 = left sideline -> right sideline)': 'end_x',
          'End Y (0 -> 1 = back of opponent endzone -> back of own endzone)': 'end_y'
      }, axis=1, inplace=True)

    for game_id, df in self.possessions_dfs.items():
      df.rename({
          'Start X (0 -> 1 = left sideline -> right sideline)': 'start_x',
          'Start Y (0 -> 1 = back of opponent endzone -> back of own endzone)': 'start_y',
          'End X (0 -> 1 = left sideline -> right sideline)': 'end_x',
          'End Y (0 -> 1 = back of opponent endzone -> back of own endzone)': 'end_y'
      }, axis=1, inplace=True)

    for game_id in self.games.keys():
      self.passes_dfs[game_id].columns = self._clean_column_names(self.passes_dfs[game_id].columns)
      self.possessions_dfs[game_id].columns = self._clean_column_names(self.possessions_dfs[game_id].columns)
      self.points_dfs[game_id].columns = self._clean_column_names(self.points_dfs[game_id].columns)
      self.player_stats_dfs[game_id].columns = self._clean_column_names(self.player_stats_dfs[game_id].columns)
      self.blocks_dfs[game_id].columns = self._clean_column_names(self.blocks_dfs[game_id].columns)

  @staticmethod
  def _clean_column_names(columns):
    """Get more consistent and easier to use column names"""
    return columns.str.lower().str.replace(' ', '_').str.replace('?', '').str.replace("'", "")

  def _fix_player_names(self):
    """Correct inconsitencies in player names"""
    for game_id, df in self.passes_dfs.items():
      df['thrower'] = df['thrower'].replace(NAME_MAPPINGS)
      df['receiver'] = df['receiver'].replace(NAME_MAPPINGS)

    # do the same in the player stats dataframe
    for game_id, df in self.player_stats_dfs.items():
      df['player'] = df['player'].replace(NAME_MAPPINGS)

    for game_id, df in self.blocks_dfs.items():
      if "player" in df.columns: # are there any blocks for this game?
        df['player'] = df['player'].replace(NAME_MAPPINGS)

  def _transform_pass_stats(self):
    """Convert the passing numbers to terms that we're interested in. i.e. location -> distance to endzone"""
    for game_id, df in self.passes_dfs.items():
      df['dist_to_endzone_end'] = df['end_y']*100-18
      df['dist_to_endzone_start'] = df['start_y']*100-18
      df['dist_to_endzone_end_capped'] = df['dist_to_endzone_end'].clip(lower=0)
      df['start_x_m'] = df['start_x']*36
      df['end_x_m'] = df['end_x']*36
      df['dist_from_middle_start'] = abs(18-df['start_x_m'])
      df['dist_from_middle_end'] = abs(18-df['end_x_m'])
      df['dist_from_sideline_start'] = abs( 18-df['dist_from_middle_start'])
      df['dist_from_sideline_end'] = abs(18-df['dist_from_middle_end'])
      df['dist_from_middle_diff'] = df['dist_from_middle_start'] - df['dist_from_middle_end']
      df['dist_to_endzone_diff'] = df['dist_to_endzone_start'] - df['dist_to_endzone_end_capped']

      assert (df.loc[df['assist']==1, 'dist_to_endzone_end'] < 0).all()
      assert (df.loc[:,'dist_to_endzone_start'] >= 0).all()
      assert (df.loc[:,'dist_from_sideline_start'] <= 18).all()
      assert (df.loc[:,'dist_from_sideline_start'] >= 0).all()

    for game_id, df in self.possessions_dfs.items():
      df['dist_to_endzone_start'] = df['start_y']*100-18
      df['start_x_m'] = df['start_x']*36
      df['dist_from_middle_start'] = abs(18-df['start_x_m'])
      df['dist_from_sideline_start'] = abs( 18-df['dist_from_middle_start'])

  def _transform_points_played(self):
    """Make data frames with players and each point they played on the rows."""
    for game_id, df in self.player_stats_dfs.items():
        # Ensure 'points_played' is treated as a string before splitting
        df['points_played'] = df['points_played'].astype(str)
        # Split the 'points_played' string and explode into new rows
        transformed_df = df.assign(points_played=df['points_played'].str.split(',')).explode('points_played')[['player', 'points_played']]
        # Convert the exploded 'points_played' column to integer
        transformed_df['points_played'] = pd.to_numeric(transformed_df['points_played'], errors='coerce').astype('Int64')
        # Remove rows where points_played is NaN (from splitting empty strings etc.)
        transformed_df.dropna(subset=['points_played'], inplace=True)
        self.player_points_dfs[game_id] = transformed_df

  def compute_all_stats(self):
    self._transform_points_played()
    self._compute_game_stats()
    self._compute_scoring_probabilities()
    self._compute_gpa()
    self._compute_player_game_stats()
    self.aggregated_player_stats_df = self._compute_player_stats(self.aggregated_player_stats_df)
    self.game_player_stats_df = self._compute_player_stats(self.game_player_stats_df)

  def _compute_scoring_probabilities(self):
    """Use the possession model to compute the starting and ending probabilites of scoring for each pass."""
    for game_id, df in self.passes_dfs.items():
      df['scoring_prob_start'] = self.point_model.predict(sm.add_constant(df[['dist_to_endzone_start', 'dist_from_sideline_start']]))
      df['scoring_prob_end'] = self.point_model.predict(sm.add_constant(df[['dist_to_endzone_end_capped', 'dist_from_sideline_end']]))
      df['scoring_prob_end'] = df.apply(lambda x: 1 if x['assist']==1 else x['scoring_prob_end'], axis=1)
      df['scoring_prob_end'] = df.apply(lambda x: 0 if x['turnover']==1 else x['scoring_prob_end'], axis=1)

      # compute opponents scoring probability
      df['opp_dist_to_endzone_end_capped'] = 70-df['dist_to_endzone_end_capped']
      df['scoring_prob_end_opp'] = self.point_model.predict(sm.add_constant(df[['opp_dist_to_endzone_end_capped', 'dist_from_sideline_end']]))

    for game_id, df in self.possessions_dfs.items():
      df['scoring_prob_start'] = self.possession_model.predict(sm.add_constant(df[['dist_to_endzone_start', 'dist_from_sideline_start']]))

  def _compute_gpa(self):
    """Compute the goal probability added"""
    for game_id, df in self.passes_dfs.items():
      df['game_efficiency'] = self.get_game_efficiency(game_id)
      df['gpa'] = df['scoring_prob_end'] - df['scoring_prob_start']
      df['gpa'] = df.apply(self._make_turnover_correction, axis=1)
      df['thrower_gpa'] = df.apply(lambda x: self._assign_credit(x, 'thrower'), axis=1)
      df['receiver_gpa'] = df.apply(lambda x: self._assign_credit(x, 'receiver'), axis=1)

  def _compute_max_gpa_all(self):
    """For each player, compute the maximum GPA which you could have had."""
    max_gpas_df = pd.DataFrame()
    for game_id, df in self.possessions_dfs.items():
      player_possessions_df = pd.merge(self.player_points_dfs[game_id], df, left_on='points_played', right_on='point', how='left')
      result = (player_possessions_df.groupby('player')['scoring_prob_start'].sum()/2).reset_index() # divide by 2 because you can't do it all yourself
      result['game'] = GAMES[game_id]
      max_gpas_df = pd.concat([max_gpas_df, result], ignore_index=True)
    max_gpas_df.columns = ['player', 'game', 'max_gpa']
    return max_gpas_df

  def _compute_max_gpa_game(self, game_id):
      """For each player, compute the maximum GPA which you could have had for a single game."""
      possession_df = self.possessions_dfs[game_id]
      player_possessions_df = pd.merge(self.player_points_dfs[game_id], possession_df, left_on='points_played', right_on='point', how='left')
      result = (player_possessions_df.groupby('player')['scoring_prob_start'].sum()/2).reset_index() # divide by 2 because you can't do it all yourself
      result.columns = ['player', 'max_gpa']
      return result

  def _compute_player_game_stats(self):
      """Aggregate individual player stats across all games."""
      all_passes = []
      # make a dataframe which will have stats broken out on a game level
      self.game_player_stats_df = pd.DataFrame()
      for game_id, df in self.passes_dfs.items():
          offensive_game_stats = self._aggregate_offensive_player_stats(df, game=GAMES[game_id])
          defensive_game_stats = self._aggregate_defensive_player_stats(game_id=game_id)
          if defensive_game_stats is not None:
            game_stats = offensive_game_stats.merge(defensive_game_stats, on="player", how="left")
          else:
            game_stats = offensive_game_stats

          self.game_player_stats_df = pd.concat([self.game_player_stats_df, game_stats])

      self.aggregated_player_stats_df = self._aggregate_player_stats_all_games(self.game_player_stats_df)

  def _aggregate_defensive_player_stats(self, game_id=None):

    def _compute_defensive_blocks(self, game_id):
      blocks_df = self.blocks_dfs[game_id]
      # count up the blocks for each player
      blocks_df = blocks_df.groupby("player").count()

      # a row only gets created here when there's a block, so you can use any column you want as the blocks column
      blocks_df.rename(columns={"created": "blocks"}, inplace=True)

      # fill in all other players who played this game with 0 blocks
      all_players = self.player_stats_dfs[game_id]['player'].values
      blocks_df = blocks_df.reindex(all_players, fill_value=0)
      blocks_df.reset_index(inplace=True)
      return blocks_df[['player', 'blocks']]

    def _compute_defensive_gpa(self, blocks_df, game_id):
      blocks_df['game_efficiency'] = self.get_game_efficiency(game_id)
      blocks_df['total_defensive_gpa'] = blocks_df['blocks'] * blocks_df['game_efficiency']
      return blocks_df

    if len(self.blocks_dfs[game_id]) == 0:
      return None

    blocks_df = _compute_defensive_blocks(self, game_id)
    blocks_df = _compute_defensive_gpa(self, blocks_df, game_id)
    return blocks_df

  def _aggregate_offensive_player_stats(self, passes_df, game=None):
    """Given an aggregated player dataframe, compute some extra stats."""

    # Compute adjusted turnovers based on blame
    passes_df['thrower_turnover_credit'] = passes_df.apply(lambda row: (
        0.5 if row['thrower_error'] == 1 and row['receiver_error'] == 1 else
        1 if row['thrower_error'] == 1 else
        0
    ) if row['turnover'] == 1 else 0, axis=1)
    passes_df['receiver_turnover_credit'] = passes_df.apply(lambda row: (
        0.5 if row['thrower_error'] == 1 and row['receiver_error'] == 1 else
        1 if row['receiver_error'] == 1 else
        0
    ) if row['turnover'] == 1 else 0, axis=1)

    # start with all players as an axis just in case some players don't have a pass in a game
    stats_df = pd.DataFrame(index=self.get_players())

    # ----- Thrower Stats -----
    completed_thrower_df = passes_df.query('turnover==0').groupby('thrower').agg({
        'assist': 'sum',
        'secondary_assist': 'sum',
        'huck': 'sum',
        'swing': 'sum',
        'dump': 'sum',
        'dist_to_endzone_diff': 'sum',
        'dist_from_middle_diff': 'sum',
        'thrower': 'count',
        'thrower_gpa': 'sum'
    }).rename_axis('player') \
      .rename({
          'dist_to_endzone_diff': 'throwing_meters_gained',
          'dist_from_middle_diff': 'throwing_centering_meters_gained',
          'assist': 'assists',
          'huck': 'hucks_completed',
          'secondary_assist': 'secondary_assists',
          'swing': 'swings',
          'dump': 'dumps',
          'thrower': 'completions',
          'thrower_gpa': 'thrower_gpa_no_turns'
      }, axis='columns').reset_index()

    thrower_df = passes_df.groupby('thrower').agg({
        'thrower_turnover_credit': 'sum',
        'thrower_gpa': 'sum',
        'thrower': 'count',
        'huck': 'sum',
    }).rename_axis('player').rename({
        'thrower_turnover_credit': 'turnover_thrown',
        'thrower': 'throws',
        'huck': 'hucks_thrown',
    }, axis='columns').reset_index()


    thrower_df = pd.merge(thrower_df, completed_thrower_df, on='player', how='outer')
    thrower_df.fillna(0, inplace=True)

    # ----- Receiver Stats -----
    completed_receiver_df = passes_df.query('turnover==0').groupby('receiver').agg({
        'assist': 'sum',
        'huck': 'sum',
        'receiver': 'count',
        'dist_to_endzone_diff': 'sum',
        'dist_from_middle_diff': 'sum',
        'receiver_gpa': 'sum'
    }).rename_axis('player')\
      .rename({
          'dist_to_endzone_diff': 'receiving_meters_gained',
          'dist_from_middle_diff': 'receiving_centering_meters_gained',
          'receiver': 'catches',
          'assist': 'goals',
          'huck': 'hucks_received',
          'receiver_gpa': 'receiver_gpa_no_turns'
      }, axis='columns').reset_index()

    receiver_df = passes_df.groupby('receiver').agg({
        'receiver_turnover_credit': 'sum',
        'receiver_gpa': 'sum',
    }).rename_axis('player').rename({
        'receiver_turnover_credit': 'receiver_turnovers'
    }, axis='columns').reset_index()

    receiver_df = pd.merge(receiver_df, completed_receiver_df, on='player', how='outer')
    receiver_df.fillna(0, inplace=True)

    # ----- Merge Thrower and Receiver Stats -----
    stats_df = pd.merge(stats_df, thrower_df, left_index=True, right_on='player', how='outer')
    stats_df = pd.merge(stats_df, receiver_df, on='player', how='outer', suffixes=('_as_thrower', '_as_receiver'))
    stats_df.fillna(0, inplace=True)

    # Add total GPA, total turnovers, touches
    stats_df['total_offensive_gpa'] = stats_df['thrower_gpa'] + stats_df['receiver_gpa']
    stats_df['total_offensive_gpa_no_turns'] = stats_df['thrower_gpa_no_turns'] + stats_df['receiver_gpa_no_turns']
    stats_df['total_turnovers'] = stats_df['turnover_thrown'] + stats_df['receiver_turnovers']
    stats_df['touches'] = stats_df['throws'] + stats_df['goals']
    stats_df['game'] = game

    # compute the max GPA for the game and then join
    game_id = {v: k for k, v in GAMES.items()}[game]
    max_gpas_df = self._compute_max_gpa_game(game_id=game_id)
    stats_df = stats_df.merge(max_gpas_df, on='player', how='left')

    return stats_df

  def _aggregate_player_stats_all_games(self, df):
    """Take the individual games and aggregate them together."""
    result_df = df.groupby('player').sum().reset_index()
    # compute the number of games
    result_df['games'] = df.groupby('player')['game'].nunique().values
    return result_df

  def _assign_credit(self, row, thrower_or_receiver):
    if thrower_or_receiver == 'thrower':
      if row['thrower_error']==1 & row['receiver_error']==1:
        return row['gpa']/2
      elif row['thrower_error']==1:
        return row['gpa']
      elif row['receiver_error']==1:
        return 0
      else:
        return row['gpa']/2
    elif thrower_or_receiver == 'receiver':
      if row['thrower_error']==1 & row['receiver_error']==1:
        return row['gpa']/2
      if row['receiver_error']==1:
        return row['gpa']
      elif row['thrower_error']==1:
        return 0
      else:
        return row['gpa']/2

